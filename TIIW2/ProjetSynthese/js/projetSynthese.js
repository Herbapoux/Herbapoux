//Natation

$("#natationShow").hover(function () {
    $('.containerFiltre').css("cursor", "pointer");

    $('#natationHide').show();
    $('#natationShow').hide();
});

$('#natationHide').mouseleave(function () {
    $('.containerFiltre').css("cursor", "pointer");

    $('#natationHide').hide();
    $('#natationShow').show();
});


